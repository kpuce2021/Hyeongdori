package com.kpu.domain;

public class ResultVO {
	String id;
	String imgName;
	String uncut;
	String spalling;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getImgName() {
		return imgName;
	}
	public void setImgName(String imgName) {
		this.imgName = imgName;
	}
	public String getUncut() {
		return uncut;
	}
	public void setUncut(String uncut) {
		this.uncut = uncut;
	}
	public String getSpalling() {
		return spalling;
	}
	public void setSpalling(String spalling) {
		this.spalling = spalling;
	}
	
}
